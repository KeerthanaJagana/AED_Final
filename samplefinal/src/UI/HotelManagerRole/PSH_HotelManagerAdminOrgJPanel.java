    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        Object row[] = new Object[10];

        PSH_location location1 = enterpriseAdmin.findServiceLocation(location.getName());
        PSH_EnterpriseCatalogDirectory businessCatalogueDirectory = location.getEnterpriseCatalogDirectory();
        if (businessCatalogueDirectory == null) {
            return;
        }
        for (PSH_EnterCatag_Hotel hotel : businessCatalogueDirectory.getListOfHotels()) {
            if (hotel.findManager(user) != null) {
                if (hotel.getTourGuideORG() != null) {
                    row[0] = "TourGuide";
                    for (PSH_TourGuideOrg tourGuide : hotel.getTourGuideORG()) {
                        for (PSH_Manager supr : tourGuide.getListOfManager()) {       //add supr 
                            row[0] = location1.getName();
                            row[1] = "TourGuide";
                            row[2] = tourGuide.getName();
                            row[3] = supr.getName();
                            row[4] = supr.getUsername();
                            row[5] = supr.getPassword();
                            model.addRow(row);
                        }
                    }
                }
                if (hotel.getSafariOrg() != null) {
                    row[0] = "SafariService";
                    for (PSH_SafariOrg carService : hotel.getSafariOrg()) {
                        for (PSH_Manager supr : carService.getListOfManager()) {       //add supr 
                            row[0] = location1.getName();
                            row[1] = "SafariService";
                            row[2] = carService.getName();
                            row[3] = supr.getName();
                            row[4] = supr.getUsername();
                            row[5] = supr.getPassword();
                            model.addRow(row);
                        }
                    }
                }
            }
        }
    }
    
